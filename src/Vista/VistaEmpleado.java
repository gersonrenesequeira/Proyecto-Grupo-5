    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
         try {
            if (textBuscarEmpleado.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ingrese un nombre para buscar.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String nombreBuscado = textBuscarEmpleado.getText().trim().toLowerCase(); // Convertir a minúsculas
            List<Empleado> empleados = empleadoController.obtenerTodosEmpleados();
            List<Empleado> empleadosFiltrados = new ArrayList<>();

            if (empleados != null) {
                for (Empleado emp : empleados) {
                    if (emp.getNombre().toLowerCase().contains(nombreBuscado)) { // Búsqueda parcial
                        empleadosFiltrados.add(emp);
                    }
                }
            }

            modeloTabla.setRowCount(0); // Limpiar la tabla
            if (!empleadosFiltrados.isEmpty()) {
                for (Empleado emp : empleadosFiltrados) {
                    Object[] fila = {
                        emp.getIdEmpleado(), // Columna oculta
                        emp.getNombre(),
                        emp.getApellido(),
                        emp.getCedula(),
                        emp.getTelefono() != null ? emp.getTelefono() : "",
                        emp.getDireccion() != null ? emp.getDireccion() : "",
                        emp.getCorreo() != null ? emp.getCorreo() : "",
                        emp.getRol()
                    };
                    modeloTabla.addRow(fila);
                }
            } else {
                JOptionPane.showMessageDialog(this, "El empleado que ingresaste no existe.", "Error", JOptionPane.ERROR_MESSAGE);
                cargarEmpleados(); // Restaurar la tabla completa si no hay coincidencias
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar empleado: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

