      private void inicializarTabla() {
        String[] columnas = {"ID", "Nombre", "Apellido", "Cedula", "Telefono", "Direccion", "Correo", "Rol"};
        modeloTabla = new DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // Hacer la tabla no editable
            }
        };
        tablaEmpleados.setModel(modeloTabla);
        // Ocultar la columna ID (índice 0)
        tablaEmpleados.getColumnModel().getColumn(0).setMinWidth(0);
        tablaEmpleados.getColumnModel().getColumn(0).setMaxWidth(0);
        tablaEmpleados.getColumnModel().getColumn(0).setWidth(0);
    }
      
         private void cargarEmpleados() {
        try {
            List<Empleado> empleados = empleadoController.obtenerTodosEmpleados();
            modeloTabla.setRowCount(0); // Limpiar la tabla
            if (empleados != null) {
                for (Empleado emp : empleados) {
                    Object[] fila = {
                        emp.getIdEmpleado(), // Columna oculta
                        emp.getNombre(),
                        emp.getApellido(),
                        emp.getCedula(),
                        emp.getTelefono() != null ? emp.getTelefono() : "",
                        emp.getDireccion() != null ? emp.getDireccion() : "",
                        emp.getCorreo() != null ? emp.getCorreo() : "",
                        emp.getRol()
                    };
                    modeloTabla.addRow(fila);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar empleados: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
        
